package F_literals;

public class b_character_escape_sequence {
    public static void main(String[] args) {
        System.out.println("Hello Sanothimi! \"Sanothimi, Bhaktapur\"");
        System.out.println("Hello Sanothimi! \nSanothimi, Bhaktapur");
        System.out.println("Hello Sanothimi! \tSanothimi, Bhaktapur");
        System.out.println("Hello Sanothimi! \bSanothimi, Bhaktapur");
        System.out.println("Hello Sanothimi! \rSanothimi, Bhaktapur");
        System.out.println("Hello Sanothimi! \fSanothimi, Bhaktapur");
    }
}
